primeiro_numero = float(input('Digite o primeiro número: '))
segundo_numero = float(input('Digite o segundo número: '))

soma = primeiro_numero + segundo_numero
subtracao = primeiro_numero - segundo_numero
divisao = primeiro_numero / segundo_numero
multiplicacao = primeiro_numero * segundo_numero

print (f'A soma do {primeiro_numero} com o {segundo_numero} é igual a {soma}')
print (f'A subtracao do {primeiro_numero} com o {segundo_numero} é igual a {subtracao}')
print (f'A divisao do {primeiro_numero} com o {segundo_numero} é igual a {divisao}')
print (f'A multiplicacoa do {primeiro_numero} com o {segundo_numero} é igual a {multiplicacao}')